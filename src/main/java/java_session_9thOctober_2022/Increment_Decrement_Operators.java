package java_session_9thOctober_2022;

public class Increment_Decrement_Operators {

	public static void main(String[] args) {
		// ++ (increment operator)
		// -- (decrement operator)
		
		//post increment, pre increment
		//post decrement, pre decrement
		
		/*
		 * int i = 1; 
		 * i++; // it is post increment operator 
		 * ++i; // it is pre increment operator 
		 * i--; // it is post decrement operator 
		 * --i; // it is pre decrement operator
		 */
		
		//Ctrl + Shift + / (this is to comment multiple lines of code)
		//Ctrl + Shift + \ (this is to uncomment multiple lines of code)
		
		
		int i = 1;
		System.out.println(i); //1
		i++;
		System.out.println(i); //2
		++i;
		System.out.println(i); //3
		i--;
		System.out.println(i);//2
		--i;
		System.out.println(i); //1
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
